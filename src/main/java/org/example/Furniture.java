package org.example;

public class Furniture {
    final String name;
    final double width;
    final double height;
    final double depth;
    final double weight;
    final String material;

    public Furniture(String name, double width, double height, double depth, double weight, String material) {
        this.name = name;
        this.width = width;
        this.height = height;
        this.depth = depth;
        this.weight = weight;
        this.material = material;
    }
}
